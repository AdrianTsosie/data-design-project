	<h1>User Story</h1>

		<p>As a user, I need to be able to find the music I'm looking for in a timely manner.</p>

		<h2>Use Case</h2>
			<ul>
				<li><strong>Title:</strong>Looking for new song to play at gigs</li>
				<li><strong>Description:</strong>Get Right! wants to find new songs for his collection</li>
				<li><strong>Name of user and their roles:</strong></li>
					<ul>
						<li>DJ Get Right!</li>
						<li>DJ always looking for the newest songs out</li>
					</ul>
				<li><strong>Usage pre-conditions:</strong> Must have subscription to Record Pool.</li>
				<li><strong>Usage post conditions:</strong> All tracks selected will be downloaded.</li>
			</ul>

			<h3>Interaction Flow</h3>
				<ul>
					<li>Get Right! enters djcity.com into the browser.</li>
					<li>Server returns DJ City's main page.</li>
					<li>He then clicks on the record pool tab.</li>
					<li>He then must login to his account.</li>
					<li>Server returns a login screen</li>
					<li>He then logs in.</li>
					<li>Now he can browse for songs on the site.</li>
					<li>Once he finds a song he likes he clicks download.</li>
					<li>Server completes request and file is downloaded to his desktop.</li>
					<li>Once downloaded the song now shows in the song list as downloaded.</li>
				</ul>

